package se.fredin.gdxtensions.object.weapon;

/**
 * Simple enum for deciding what type of firing mode a weapon should have
 * @author Johan Fredin
 */
public enum FiringMode {
	
	/** Semi automatic, meaning only one projectile per button press */
	SEMI,
	/**  Automatic, meaning projectiles will keep comming out while button is pressed */
	AUTOMATIC;

}
